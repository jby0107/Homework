def ParityCheck(Input):
    Count = 0
    for i in Input:
        Count += i
    if Count % 2 == 1:
        print('Data correct')
    else:
        print('Error')
        
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
    